<p id="welcome2">平成11年に開院して以降、雑誌などにも度々取り上げられ、千葉全域から東京近郊、遠方からも健康管理やリラクゼーション目的の方々が多く来院されております。<br />
腰が痛くても腰に直接原因があるとは限らず、極端に言えば、足の指一本のケガでも、歩き方が変わってしまい、体に歪みが生じます。<br />
そこから腰痛に発展しまう事もあるのです。<br />
そういった痛みの根本的な原因を見つけ出し、オリジナル技法によって関連する筋肉や靭帯を優しく緩めて、背骨・骨盤を整えていきます。<br />
的確なカウンセリングと施術後のケアアドバイスも好評で、野球選手・力士などのスポーツ選手から、モデル、医者・看護士の方まで幅広いジャンルの方々が月500人以上来院される人気店でもあります。<br />
無理な矯正は行わないので、お年寄りからお子様まで安心してお受け頂けます。<br />
（施術中、気持ち良くて寝てしまう方も多い程です。）<br />
『どこへ行っても良くならない』という方、1回の施術でもその違いが解って頂けると思いますので、是非一度お試し下さい。</p>

